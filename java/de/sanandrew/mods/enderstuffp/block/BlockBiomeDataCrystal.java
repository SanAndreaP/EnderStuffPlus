package de.sanandrew.mods.enderstuffp.block;

import de.sanandrew.mods.enderstuffp.tileentity.TileEntityBiomeDataCrystal;
import net.minecraft.block.Block;
import net.minecraft.block.ITileEntityProvider;
import net.minecraft.block.material.Material;
import net.minecraft.entity.player.EntityPlayer;
import net.minecraft.tileentity.TileEntity;
import net.minecraft.util.ChatComponentText;
import net.minecraft.world.World;

public class BlockBiomeDataCrystal
        extends Block
        implements ITileEntityProvider
{
    public BlockBiomeDataCrystal() {
        super(Material.glass);
    }

    @Override
    public int getRenderBlockPass() {
        return 1;
    }

    @Override
    public boolean isOpaqueCube() {
        return false;
    }

    @Override
    public boolean renderAsNormalBlock() {
        return false;
    }

    @Override
    public TileEntity createNewTileEntity(World world, int meta) {
        return new TileEntityBiomeDataCrystal();
    }

    @Override
    public boolean onBlockActivated(World p_149727_1_, int p_149727_2_, int p_149727_3_, int p_149727_4_, EntityPlayer p_149727_5_, int p_149727_6_, float p_149727_7_, float p_149727_8_, float p_149727_9_) {
        p_149727_5_.addChatMessage(new ChatComponentText(String.format("%d - %d",
                                                                       ((TileEntityBiomeDataCrystal)p_149727_1_.getTileEntity(p_149727_2_, p_149727_3_, p_149727_4_)).getBiomeID(),
                                                                       ((TileEntityBiomeDataCrystal)p_149727_1_.getTileEntity(p_149727_2_, p_149727_3_, p_149727_4_)).dataProgress)));
        return super.onBlockActivated(p_149727_1_, p_149727_2_, p_149727_3_, p_149727_4_, p_149727_5_, p_149727_6_, p_149727_7_, p_149727_8_, p_149727_9_);
    }
}
